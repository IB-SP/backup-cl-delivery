<div class="how_to_prepaire_text">
<span>3.</span> <p>Spread dough into skillet in an even layer. Top with more chocolate chips and sprinkle with flaky sea salt. Bake 15 to 20 minutes. Serve warm with ice cream, warm caramel sauce and chocolate syrup, if desired.</p>
