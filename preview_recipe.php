<ul>
<li>- 1 tbsp Good eggwhites </li>
<li>- 1 tablespoon balsamic vinegar </li>
<li>- 1 tsp mustard </li>
<li>- 1 dried tomato (chopped) </li>
<li>- 1/2 tablespoon olive oil</li> 
<li>- 2 g Birkenzucker (xylitol) </li>
<li>- 1 pinch of salt </li>
<li>- 1 pinch of pepper</li>
